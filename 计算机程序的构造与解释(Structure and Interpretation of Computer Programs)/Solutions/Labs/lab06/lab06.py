# 迭代写法。在python中这样写效率高一些。
def link_to_list(link):
    """Takes a linked list and returns a Python list with the same elements.

    >>> link = Link(1, Link(2, Link(3, Link(4))))
    >>> link_to_list(link)
    [1, 2, 3, 4]
    >>> link_to_list(Link.empty)
    []
    """
    result = []
    cur_node = link
    while cur_node != Link.empty:
        result.append(cur_node.first)
        cur_node = cur_node.rest
    return result


# 递归写法。尽管这在python中效率不高，但在Scheme里效率就会很不错，而且还简单。
def link_to_list_rec(link):
    if link is Link.empty:
        return []
    return [link.first] + link_to_list_rec(link.rest)


def store_digits(n):
    """Stores the digits of a positive number n in a linked list.

    >>> s = store_digits(1)
    >>> s
    Link(1)
    >>> store_digits(2345)
    Link(2, Link(3, Link(4, Link(5))))
    >>> store_digits(876)
    Link(8, Link(7, Link(6)))
    """
    result = Link.empty
    while n > 0:
        result = Link(n % 10, result)
        n //= 10
    return result


